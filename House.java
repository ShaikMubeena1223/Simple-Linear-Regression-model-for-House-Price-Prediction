public class House {
    double area;
    int bedrooms;
    int bathrooms;
    int age;
    double price;

    public House(double area, int bedrooms, int bathrooms, int age, double price) {
        this.area = area;
        this.bedrooms = bedrooms;
        this.bathrooms = bathrooms;
        this.age = age;
        this.price = price;
    }
}